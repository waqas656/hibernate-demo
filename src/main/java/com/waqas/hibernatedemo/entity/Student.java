package com.waqas.hibernatedemo.entity;

import jakarta.persistence.*;

@Entity
@Table(name = "student")
@NamedQuery(name = "Student_findByFirstName",
        query = "FROM Student s WHERE s.firstName = :firstName")
@NamedQuery(name = "Student_findByFirstAndLastName",
        query = "FROM Student s WHERE s.firstName = ?1 AND s.lastName = ?2")
@NamedNativeQuery(name = "Student_findByLastName",
        query = "SELECT * FROM student WHERE last_name = ?", resultClass = Student.class)
public class Student {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private Integer id;

    @Column(name = "first_name")
    private String firstName;

    @Column(name = "last_name")
    private String lastName;

    @Column(name = "email")
    private String email;

    public Student() {
    }

    public Student(String firstName, String lastName, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", firstName='" + firstName + '\'' +
                ", lastName='" + lastName + '\'' +
                ", email='" + email + '\'' +
                '}';
    }
}
